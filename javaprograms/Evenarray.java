public class Evenarray {
    public static void main(String[] args)
    {
        int[] a={1,2,3,4,5,6,8};
        int i;
        for(i=0;i<7;i++)
        {
            if(a[i]%2==0)
            {
                System.out.println("even number"+a[i]);
            }
        }
    }
    
}
